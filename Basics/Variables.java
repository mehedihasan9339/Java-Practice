package Basics;

public class Variables {
    public static void main(String[] args) {
        int age = 25; // integer type
        double salary = 50000.50; // double type
        char grade = 'A'; // character type
        boolean isEmployed = true; // boolean type
        String name = "John Doe"; // String type

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Grade: " + grade);
        System.out.println("Employed: " + isEmployed);
    }
}



// Variables
// Variables are containers for storing data values. In Java, each variable must be declared with a data type.

// Data Types
// Primitive Data Types: int, float, double, char, boolean, byte, short, long
// Non-Primitive Data Types: String, Arrays, Classes, etc.